var userTable = document.getElementById("userTable").getElementsByTagName('tbody')[0];
var userName = document.getElementById("name");
var fatherName = document.getElementById("fatherName");
var rollno = document.getElementById("rollno");
var age = document.getElementById("age");
var profilePic = document.getElementById("profilePic");
var options = document.getElementById("options");

function addData() {
    userTable.innerHTML = "";
    for (var i = 0; i < userData.length; i++) {
        userTable.innerHTML += `
<tr>
<td>${i + 1}</td>
<td><img src="${userData[i].profilePic}" class="profile-pic" alt="Profile Picture"></td>
<td>${userData[i].name}</td>
<td>${userData[i].fatherName}</td>
<td>${userData[i].rollNo}</td>
<td>${userData[i].status}</td>
<td class="heart-icon" onclick="likeUser(this)"><i class="fa-regular fa-heart"></i></td>
</tr>
`;
    }
}
addData();

function addUser() {
    var obj = {
        name: userName.value,
        fatherName: fatherName.value,
        rollNo: rollno.value,
        age: age.value,
        status: options.value,
        profilePic: profilePic.value // Add profile picture URL
    };

    userData.push(obj);
    addData();
}

function likeUser(element) {
    if (element.firstElementChild.className === "fa-regular fa-heart") {
        element.innerHTML = '<i class="fa-solid fa-heart"></i>';
    } else {
        element.innerHTML = '<i class="fa-regular fa-heart"></i>';
    }
}